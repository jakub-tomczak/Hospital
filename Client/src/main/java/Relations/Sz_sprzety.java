package Relations;

public class Sz_sprzety {
  private String id;
  private String nazwa;
  private String rokprodukcji;
  private String stan;
  private String sz_oddzialy_id;

  public String getId() {
    return id;
  }

  public void setId(String id) {
    this.id = id;
  }

  public String getNazwa() {
    return nazwa;
  }

  public void setNazwa(String nazwa) {
    this.nazwa = nazwa;
  }

  public String getRokprodukcji() {
    return rokprodukcji;
  }

  public void setRokprodukcji(String rokprodukcji) {
    this.rokprodukcji = rokprodukcji;
  }

  public String getStan() {
    return stan;
  }

  public void setStan(String stan) {
    this.stan = stan;
  }

  public String getSz_oddzialy_id() {
    return sz_oddzialy_id;
  }

  public void setSz_oddzialy_id(String sz_oddzialy_id) {
    this.sz_oddzialy_id = sz_oddzialy_id;
  }
}
